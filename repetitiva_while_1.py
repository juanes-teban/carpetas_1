'''
repetitiva_while_1.py
script en Python que muestre los numeros del 0 al 13 utilizando la estructura repetitiva "while"
'''
print('Programa que muestra los nuymeros del 0 al 13')
#contador
numero=0

while numero <= 13:
    print(f'contador: {numero}')
    numero = numero + 1
else:
    print('He terminado de contar')

print('nos vemos')     
